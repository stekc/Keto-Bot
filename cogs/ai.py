    async def models_autocompletion(
        self, interaction: Interaction, current: str
    ) -> List[app_commands.Choice[str]]:
        choices = self.models
        return [
            app_commands.Choice(name=choice, value=choice)
            for choice in choices
            if current.lower() in choice.lower()
        ]

    @commands.Cog.listener()
    async def on_message(self, message: discord.Message):
        if not message.guild:
            return
        if not await self.config_cog.get_config_value(
            message.guild.id, "transcriptions", "enabled"
        ):
            return
        user_config = self.bot.get_cog("UserConfig")
        if not await user_config.get_config_value(
            message.author.id, "transcriptions", "enabled"
        ):
            return
        if message.attachments:
            if message.attachments[0].filename == "voice-message.ogg":
                attachment = message.attachments[0]
                if attachment.size > 2097152:
                    return

                audio_data = AudioSegment.from_ogg(io.BytesIO(await attachment.read()))

                if len(audio_data) > 30000:
                    return

                loading_embed = discord.Embed(
                    color=await get_color(message.author.avatar.url),
                    description="<a:discordloading:1199066225381228546> Transcribing voice message...",
                )
                loading_msg = await message.reply(
                    embed=loading_embed, mention_author=False
                )

                whisper_client = AsyncWhisper(os.getenv("OPENAI_TOKEN"))
                transcription = await whisper_client.transcribe_audio(audio_data)
                embed = discord.Embed(
                    description=transcription,
                    color=await get_color(message.author.avatar.url),
                )
                embed.set_author(
                    name="Transcribed voice message from "
                    + message.author.display_name,
                    icon_url=message.author.avatar.url,
                )
                # embed.set_footer(text=f"Powered by OpenAI Whisper")
                embed.timestamp = message.created_at
                if transcription:
                    await loading_msg.edit(embed=embed)
                else:
                    await loading_msg.delete()

    @commands.hybrid_command(
        name="chatgpt",
        description="Talk with ChatGPT.",
    )
    @app_commands.autocomplete(model=models_autocompletion)
    @app_commands.allowed_installs(guilds=True, users=True)
    @app_commands.allowed_contexts(guilds=True, dms=True, private_channels=True)
    @commands.cooldown(1, 10, commands.BucketType.user)
    async def chatgpt(
        self,
        context: Context,
        message: str,
        image: Optional[discord.Attachment] = None,
        model: str = None,
    ):
        if context.author.id != 1088593923661893703:
            return await context.send(
                "This command is currently unavailable.",
                ephemeral=True,
            )
        if not model:
            model = "gpt-4o-mini"
        if model in ["o1-mini", "o1-preview"] and image:
            return await context.send(
                "Images are not supported for o1 models.",
                ephemeral=True,
            )
        if model not in self.models:
            return await context.send(
                "Invalid model. Please choose from the available models.",
                ephemeral=True,
            )

        if re.search(self.slur_regex, message):
            embed = discord.Embed(
                title=f"{model} says:",
                description="I’m sorry, but I can't assist with that.",
                color=discord.Color.blue(),
            )
            return await context.send(embed=embed)

        async with context.typing():
            prompt = [
                {
                    "role": "user",
                    "content": [{"type": "text", "text": message}],
                }
            ]

            if image:
                prompt[0]["content"].append(
                    {
                        "type": "image_url",
                        "image_url": {
                            "url": image.url,
                        },
                    }
                )

            completion = await self.openai.chat.completions.create(
                model=model,
                messages=prompt,
            )
            response = completion.choices[0].message.content

            if re.search(self.slur_regex, response):
                response = "I’m sorry, but I can't assist with that."

            if len(response) > 4096:
                messages = []
                for i in range(0, len(response), 4096):
                    embed = discord.Embed(
                        description=response[i : i + 4096], color=discord.Color.blue()
                    )
                    if i == 0:
                        embed.title = f"{model} says:"
                    msg = await context.send(embed=embed)
                    messages.append(msg)
            else:
                embed = discord.Embed(description=response, color=discord.Color.blue())
                embed.title = f"{model} says:"
                msg = await context.send(embed=embed)


